package gen

import (
	"fmt"
	"go/format"
	"sort"
	"strings"

	"xsd2go-lite2/internal/parse"
)

// Generate produces a Go source file string for the given types.
func Generate(types []*parse.ComplexType, pkgName string, skipAbstract bool, withDoc bool, mapNS map[string]string, xsdSource string, version string, attrGroups map[string]*parse.AttrGroup) (string, error) {
	aliases := make(map[string]string)
	importPaths := make(map[string]string)
	aliasToNS := make(map[string]string)
	for ns, pkgPath := range mapNS {
		alias := deriveAlias(pkgPath)
		if prevNS, exists := aliasToNS[alias]; exists && prevNS != ns {
			return "", fmt.Errorf("alias collision: namespaces %q and %q both derive alias %q; use different package paths", prevNS, ns, alias)
		}
		aliases[ns] = alias
		importPaths[alias] = pkgPath
		aliasToNS[alias] = ns
	}

	sort.Slice(types, func(i, j int) bool {
		return types[i].Name < types[j].Name
	})

	// Collect attributeGroup structs referenced by the types (in stable order).
	usedAliases := make(map[string]bool)
	var structsSB strings.Builder
	writeAttrGroupStructs(&structsSB, types, attrGroups, withDoc, aliases, usedAliases)

	for _, ct := range types {
		if skipAbstract && ct.Abstract {
			continue
		}
		if ct.Name == "" {
			continue
		}
		writeStruct(&structsSB, ct, withDoc, aliases, usedAliases, attrGroups)
	}

	var header strings.Builder

	if version != "" || xsdSource != "" {
		srcBase := xsdSource
		if idx := strings.LastIndexAny(srcBase, "/\\"); idx >= 0 {
			srcBase = srcBase[idx+1:]
		}
		fmt.Fprintf(&header, "// Code generated by xsd2go-lite2 %s from %s. DO NOT EDIT.\n\n", version, srcBase)
	}

	header.WriteString("package ")
	header.WriteString(pkgName)
	header.WriteString("\n\n")

	if len(usedAliases) > 0 {
		sortedAliases := make([]string, 0, len(usedAliases))
		for a := range usedAliases {
			sortedAliases = append(sortedAliases, a)
		}
		sort.Strings(sortedAliases)
		header.WriteString("import (\n")
		for _, alias := range sortedAliases {
			fmt.Fprintf(&header, "\t%s %q\n", alias, importPaths[alias])
		}
		header.WriteString(")\n\n")
	}

	src := header.String() + structsSB.String()
	formatted, err := format.Source([]byte(src))
	if err != nil {
		return src, fmt.Errorf("go/format: %w\n--- unformatted source ---\n%s", err, src)
	}
	return string(formatted), nil
}

// writeAttrGroupStructs generates struct definitions for all attributeGroups
// referenced by at least one of the given types, each emitted exactly once.
// AttributeGroups belonging to a mapped external namespace are skipped because
// they are already defined in the external package.
func writeAttrGroupStructs(sb *strings.Builder, types []*parse.ComplexType, attrGroups map[string]*parse.AttrGroup, withDoc bool, aliases map[string]string, usedAliases map[string]bool) {
	seen := make(map[string]bool)
	for _, ct := range types {
		for _, em := range ct.Embeds {
			if em.Kind != "attributeGroup" {
				continue
			}
			if _, ok := aliases[em.NS]; ok {
				continue // defined in external mapped package
			}
			key := em.NS + " " + em.XSDName
			if seen[key] {
				continue
			}
			seen[key] = true
			ag, ok := attrGroups[key]
			if !ok {
				continue
			}
			writeAttrGroupStruct(sb, ag, withDoc, aliases, usedAliases)
		}
	}
}

func writeAttrGroupStruct(sb *strings.Builder, ag *parse.AttrGroup, withDoc bool, aliases map[string]string, usedAliases map[string]bool) {
	sb.WriteString("type ")
	sb.WriteString(parse.GoTypeName(ag.Name))
	sb.WriteString(" struct {\n")
	for _, f := range ag.Fields {
		goType := f.GoType
		if alias, ok := aliases[f.TypeNS]; ok {
			goType = qualifyType(goType, alias)
			usedAliases[alias] = true
		}
		if withDoc && f.Doc != "" {
			writeComment(sb, f.Doc)
		}
		sb.WriteString("\t")
		sb.WriteString(f.GoName)
		sb.WriteString(" ")
		sb.WriteString(goType)
		sb.WriteString(" `xml:\"")
		sb.WriteString(f.XMLTag)
		sb.WriteString(buildTagSuffix(f))
		sb.WriteString("\"`\n")
	}
	sb.WriteString("}\n\n")
}

func writeStruct(sb *strings.Builder, ct *parse.ComplexType, withDoc bool, aliases map[string]string, usedAliases map[string]bool, attrGroups map[string]*parse.AttrGroup) {
	sb.WriteString("type ")
	sb.WriteString(parse.GoTypeName(ct.Name))
	sb.WriteString(" struct {\n")

	// Emit attributeGroup embeds before own fields.
	for _, em := range ct.Embeds {
		if em.Kind != "attributeGroup" {
			continue
		}
		if alias, ok := aliases[em.NS]; ok {
			sb.WriteString("\t")
			sb.WriteString(alias + "." + parse.GoTypeName(em.XSDName))
			sb.WriteString("\n")
			usedAliases[alias] = true
			continue
		}
		key := em.NS + " " + em.XSDName
		if _, ok := attrGroups[key]; !ok {
			continue
		}
		sb.WriteString("\t")
		sb.WriteString(parse.GoTypeName(em.XSDName))
		sb.WriteString("\n")
	}

	for _, f := range ct.Fields {
		goType := f.GoType
		if alias, ok := aliases[f.TypeNS]; ok {
			goType = qualifyType(goType, alias)
			usedAliases[alias] = true
		}
		if withDoc && f.Doc != "" {
			writeComment(sb, f.Doc)
		}
		sb.WriteString("\t")
		sb.WriteString(f.GoName)
		sb.WriteString(" ")
		sb.WriteString(goType)
		sb.WriteString(" `xml:\"")
		sb.WriteString(f.XMLTag)
		tag := buildTagSuffix(f)
		sb.WriteString(tag)
		sb.WriteString("\"`\n")
	}

	sb.WriteString("}\n\n")
}

func writeComment(sb *strings.Builder, doc string) {
	for _, line := range strings.Split(doc, "\n") {
		sb.WriteString("\t// ")
		sb.WriteString(strings.TrimSpace(line))
		sb.WriteString("\n")
	}
}

func buildTagSuffix(f parse.Field) string {
	if f.Omit && !f.IsChar {
		if !f.Slice {
			return ",omitempty"
		}
	}
	return ""
}
